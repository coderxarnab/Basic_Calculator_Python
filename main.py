#Taking Input from User
first_Num = input("Enter your First Number :  ")
second_Num = input("Enter your second Number :   ")

#Calculation

#SUM
Sum = float(first_Num) + float(second_Num)
print("Sum is :   " + str(Sum))

#SUBSTRACTION
Substraction = float(first_Num) - float(second_Num)
print("Substraction is :   " + str(Substraction))

#MULTIPLICATION
Multiplication = float(first_Num) * float(second_Num)
print("Multiplication is :   " + str(Multiplication))

#DIVISION & REMAINDER
Division = float(first_Num) / float(second_Num)
Remainder = float(first_Num) % float(second_Num)

print("Division is :  " + str(Division))
print("Remainder is :  "  + str(Remainder))

